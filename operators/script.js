// Aithmetic Operator
console.log(1 + 2 - 3 * 4 / 5);

// Predence Operator
// () * / + -
console.log((1 * 1) * 2 / 3 + 4 - 5);

// Assignment Operator
// = += -= /= *= %=
var x = 10;
var x = 200;
console.log(x);


// Comparison Operator
// == != === !== > < >= <=
a = 10;
b = 5;
console.log(a == b);


// Logical Operator
// && || !
console.log((4 % 2 == 0) && (2 % 2 == 0));


// String Operator
console.log(10 + "10");
console.log("10" + 10 + 10);
console.log(10 + 10 + "10");


// Typeof Operator
console.log(typeof(10));
console.log(typeof("10"));


// Ternary Operator
const s = 10;
console.log((s % 2 == 0) ? "even" : "odd");